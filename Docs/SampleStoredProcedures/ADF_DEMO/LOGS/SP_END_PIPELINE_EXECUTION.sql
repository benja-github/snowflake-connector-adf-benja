UPDATE LOGS.ADF_PIPELINE_EXECUTION_LOG
SET
     EXECUTION_STATUS = 'Success'
    ,EXECUTION_END_AT = TO_TIMESTAMP_NTZ(CURRENT_TIMESTAMP())
    ,ROWS_LOADED = $ROWS_LOADED
    ,ROWS_INSERTED = $ROWS_INSERTED
    ,ROWS_UPDATED = $ROWS_UPDATED
    ,ROWS_DELETED = $ROWS_DELETED
    ,META_UPDATED_AT = TO_TIMESTAMP_NTZ(CURRENT_TIMESTAMP())
WHERE 1 = 1
    AND ADF_PIPELINE_RUN_ID = $ADF_PIPELINE_RUN_ID
;
